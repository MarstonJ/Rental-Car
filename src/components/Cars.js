class Cars {
    constructor(make, model, noDoors, noSeats, colour, price, image) {
        this.make = make;
        this.model = model;
        this.noDoors = noDoors;
        this.noSeats = noSeats;
        this.colour = colour;
        this.price = price;
        this.image = image;
    }
}

export default Cars